export type TranslationKey = keyof typeof en;
export type TranslationParams = Readonly<Record<string, string | number>>;

const dictionaries: Record<Locale, Dictionary> = { en, ru };

export interface I18nApi {
  readonly locale: Locale;
  readonly t: (key: TranslationKey, params?: TranslationParams) => string;
  readonly agentStatus: (status: AgentSystemStatus) => string;
  readonly conversationStatus: (status: ConversationStatus) => string;
}

const agentStatusLabels: Record<Locale, Record<AgentSystemStatus, string>> = {
  en: {
    available: "Available",
    running: "Running",
    "needs-setup": "Needs setup",
    unavailable: "Not installed",
    unsupported: "Adapter missing",
  },
  ru: {
    available: "Доступен",
    running: "Запущен",
    "needs-setup": "Нужна настройка",
    unavailable: "Не установлен",
    unsupported: "Нет адаптера",
  },
};

const conversationStatusLabels: Record<Locale, Record<ConversationStatus, string>> = {
  en: {
    running: "Working",
    waiting: "Needs input",
    done: "Done",
    error: "Failed",
    idle: "Idle",
  },
  ru: {
    running: "В работе",
    waiting: "Ждёт ввод",
    done: "Готово",
    error: "Ошибка",
    idle: "Ожидает",
  },
};

const I18nContext = createContext<I18nApi | null>(null);

function format(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }
  return Object.entries(params).reduce((text, [key, value]) => text.replaceAll(`{${key}}`, String(value)), template);
}

export function translate(locale: Locale, key: TranslationKey, params?: TranslationParams): string {
  return format(dictionaries[locale][key], params);
}

export function I18nProvider({ locale, children }: { readonly locale: Locale; readonly children: ReactNode }) {
  const value = useMemo<I18nApi>(() => {
    return {
      locale,
      t: (key, params) => translate(locale, key, params),
      agentStatus: (status) => agentStatusLabels[locale][status],
      conversationStatus: (status) => conversationStatusLabels[locale][status],
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nApi {
  const value = useContext(I18nContext);
  if (value) {
    return value;
  }
  return {
    locale: "ru",
    t: (key, params) => translate("ru", key, params),
    agentStatus: (status) => agentStatusLabels.ru[status],
    conversationStatus: (status) => conversationStatusLabels.ru[status],
  };
}
